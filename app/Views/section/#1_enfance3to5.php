    <h1>Présentation</h1>
    <p>Le secteur petite enfance développe des accueils de loisirs sans hébergement (ALSH) tous les mercredis et pendant toutes les vacances
     scolaires pour les enfants de 3 à 5 ans.</p>

     <h1>Les activités</h1>

     <h1>Les mercredis</h1>
     <p>Des animateurs qualifiés proposent un large choix d’activités : grands jeux (plein air ou intérieur), des projets et découverte nature, des activités manuelles : manipulation de la pâte à sel, création de masque, de porte clé et de cadre, peinture et décoration d’intérieur, etc. Mais aussi, des sorties (art, culture et loisirs) seront organisées sur Lille et ses environs.
L’accueil du Mercredi se fait de 8h30 à 17h30 au centre de la petite enfance au 12 Place de l’Arbonnoise.</p>

    <h1>Les vacances scolaires</h1>
    <p>Semblable aux mercredis, diverses activités seront proposées aux enfants durant les vacances scolaires.
Les ALSH des vacances scolaires se font au centre de la petite enfance au 12 Place de l’Arbonnoise à Lille ; du Lundi au Vendredi de 8h30 à 17h30.</p>


