<h1>Secteur Jeune </h1>
<p>Les 12-16 ans </p>
« clic » Présentation
« clic » -> Le secteur jeune accompagne les 12-16 ans en développant des projets avec eux ; mais aussi en mettant en place diverses activités les mercredis et durant les vacances scolaires. 
Un accompagnement à la scolarité est proposé aux jeunes de la 6ème à la Terminale. Un suivi est effectué avec le collège Levi Strauss, du quartier de Vauban-Esquermes. 

<h1>Les activités</h1>

<h1>Les mercredis</h1>
<p>Un mercredi sur deux, un atelier culinaire est mis en place avec les jeunes ; et l’autre mercredi, une activité sportive,
 culturelle ou artistique sera proposée.</p>

<h1>Les vacances scolaires</h1>
<p>Des activités sont proposées durant toutes les vacances scolaires, les jeunes pourront se renseigner directement auprès de l’animateur(rice). 
Aussi, des projets « séjour » pourront être organisés avec jeunes durant les grandes vacances scolaires.
 Ils devront alors être acteurs de leur séjour. Pour cela, des autofinancements devront être mis place par les jeunes et l’accompagnant(e).</p> 

<p>
-> Que ce soit les mercredis ou pendant les vacances scolaires, les inscriptions devront se faire directement auprès de l’animateur(rice). Le tarif varie en fonction de l’activité ou du projet d’animation.
</p>

<h1>L’accompagnement à la scolarité</h1>

<p>Comme dans le secteur Enfance, les élèves de la 6ème à la Terminale peuvent être accompagnés dans leurs devoirs tous les Lundis, Mardis, Jeudis et Vendredis de 16h30 à 18h30 au sein des locaux du centre social. 
Là également, des étudiants de l’école d’ingénierie (HEI) seront présents afin d’accompagner vos enfants dans les meilleures conditions pour leurs devoirs.
<p>


