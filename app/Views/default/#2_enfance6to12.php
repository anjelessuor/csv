    <h1>Secteur Enfance</h1>
	<p>6 to 12</p>

    <h1>Présentation</h1>
    <p>Le secteur enfance développe des accueils de loisirs sans hébergement (ALSH) tous les mercredis et pendant toutes les vacances scolaires pour les enfants de 6 à 12 ans. 
        Aussi un accompagnement à la scolarité est mis en place tous les jours de la semaine sauf le mercredi.
    </p>

     <h1>Les activités</h1>

     <h1>Les mercredis</h1>
     <p>Comme pour le secteur Petite Enfance, des animateurs qualifiés proposent un large choix d’activités : grands jeux (plein air ou intérieur), des projets et découverte nature, des activités manuelles : manipulation d’argile, peinture et décoration d’intérieur, création d’accessoires sportifs, etc. Mais aussi, des sorties (art, culture et loisirs) sur Lille et ses environs.
L’accueil du Mercredi se fait de 8h30 à 17h30 au centre social de Vauban-Esquermes au 77 rue Philippe-Laurent Roland à Lille.
    </p>

    <h1>Les vacances scolaires</h1>
    <p>En plus des activités proposées le mercredi, des stages sportifs : initiation à la boxe pourront être proposés aux enfants durant les vacances scolaires. Ces stages s’effectueront avec notre partenaire le « Boxing club des Bois-Blancs ». 
Aussi, des minis-camps seront organisés durant les grandes vacances scolaires. 
Les ALSH des vacances scolaires a lieu du lundi au vendredi de 8h30 à 17h30 au centre social de Vauban-Esquermes. 
    </p>

    <h1>Accompagnement à la scolarité</h1>
    <p>Les enfants du CP à la 5ème peuvent être accompagnés dans leurs devoirs tous les Lundis, Mardis, Jeudis et Vendredis de 16h30 à 18h30 au sein des locaux du centre social. 
Ce tutorat s’effectue en partenariat avec l’école d’ingénierie (HEI) où des étudiants mettent leurs compétences en avant afin d’aider vos enfants.</p>

